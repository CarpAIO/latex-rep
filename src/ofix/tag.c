int
ofix_str_to_tag(const char *str) {
    TagStr	ts;
    
    for (ts = map; 0 != ts->tag; ts++) {
	if (0 == strcmp(ts->str, str)) {
	    return ts->tag;
	}
    }
    return 0;
}

const char*
ofix_tag_to_str(int tag) {
    TagStr	ts;
    
    for (ts = map; 0 != ts->tag; ts++) {
	if (tag == ts->tag) {
	    return ts->str;
	}
    }
    return 0;
}
